--23. Give the total weekly sales value with the date and product ID details. Use roll-up to pull the data in hierarchical order.

Select Datepart(week,date) as week_number ,date,productid,sum(sales) as Total_sales from fact group by rollup (Datepart(week,date),date,productid,sales);

