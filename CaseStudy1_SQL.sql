--1. Display the number of states present in the LocationTable.

Select Count (Distinct State) as 'Number of States' from dbo.location ;


--2. How many products are of regular type?

Select count (*) as 'No of Regular Products' from dbo.product where Type ='Regular';


--3. How much spending has been done on marketing of product ID 1?

Select Sum (Marketing) as 'Total Marketing Cost of Product Id 1' from Dbo.fact where ProductId=1


--4. What is the minimum sales of a product?

select ProductId,Min(Sales) as 'Minimum Sale value'from dbo.fact Group by ProductId order by ProductId;


--5. Display the max Cost of Good Sold (COGS).

Select Max(COGS) as 'Maximum Cost of Goods Sold' from dbo.fact;

--6. Display the details of the product where product type is coffee.

select * from  dbo.product where Product_Type = 'Coffee';


--7. Display the details where total expenses are greater than 40.

Select * from Dbo.fact where Total_Expenses > 40 order by Total_expenses asc;

--8. What is the average sales in area code 719?

Select Avg(sales) as 'Average Sales of Area Code 719 ' from dbo.fact where Area_Code = 719;

--9. Find out the total profit generated by Colorado state.

Select Sum(Profit) as 'Total Profit of Colorado State'
From dbo.fact inner join Dbo.location on dbo.fact.Area_Code = dbo.location.area_code 
Where State = 'Colorado';


--10. Display the average inventory for each product ID.

Select productId,Avg(Inventory) as 'Average Inventory' from dbo.fact group by productId order by productId;

--11. Display state in a sequential order in a Location Table.

Select distinct state  from dbo.location order by state Asc;


--12.Display the average budget of the Product where the average budget margin should be greater than 100.

Select productId,Avg(budget_margin) as 'Average Budget Margin' from dbo.fact where budget_margin >=100 group by productid ;


--13. What is the total sales done on date 2010-01-01?

Select Sum(Sales) as 'Total Sales on 01-01-2010' from dbo.fact where date Like '2010-01-01';

--14. Display the average total expense of each product ID on an individual date.

select Date,ProductId ,avg (Total_expenses) as 'Average Total Expenses' from dbo.fact group by 
date,productid ;

--15. Display the table with the following attributes such as date, productID, product_type, product, sales, profit, state, area_code.

Select f.date,f.ProductId,p.product_type,p.product,f.sales,f.profit,l.state,f.Area_Code from fact f 
inner join product p on f.productid=p.productid 
inner join location l on f.Area_Code=l.Area_Code ;


--16.Display the rank without any gap to show the sales wise rank.

Select * ,Dense_rank() over (Order by sales desc) as Sales_rank from dbo.fact

--17. Find the state wise profit and sales.
 
 Select l.state,sum(f.profit) as Total_Profit,Sum(f.sales) as Total_sales from fact f inner join location l on f.Area_Code=l.area_code group by l.state


--18. Find the state wise profit and sales along with the product name.

 Select l.state,p.product,sum(f.profit) as Total_Profit,Sum(f.sales) as Total_sales from fact f inner join location l on f.Area_Code=l.area_code
 inner join product p on f.ProductId=p.productid group by p.product,l.state


--19. If there is an increase in sales of 5%, calculate the increased sales.

Select *,Sales*1.05 as Increased_sales from fact 


--20. Find the maximum profit along with the product ID and producttype.

Select Max(profit) as 'Maximum profit',f.ProductId,p.Product_Type from fact f inner join product p on f.ProductId = p.ProductId group by f.ProductId,p.Product_Type order by [Maximum profit]desc;

